package com.tencent.cos.bean;

public class HttpHeader {
	
	
	private int timeOut;
	
	private String host;

	public int getTimeOut() {
		return timeOut;
	}

	public void setTimeOut(int timeOut) {
		this.timeOut = timeOut;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}
	

}
